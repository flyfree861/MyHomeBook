package com.example.myhomebook.Object;

public class oUser
{
    private String _name;
    private String _surname;
    private String _nickName;
    private String _bornDate;
    private String _mail;
    private String _age;


    public String get_name()
    {
        return _name;
    }

    public void set_name(String _name)
    {
        this._name = _name;
    }

    public String get_surname()
    {
        return _surname;
    }

    public void set_surname(String _surname)
    {
        this._surname = _surname;
    }

    public String get_nickName()
    {
        return _nickName;
    }

    public void set_nickName(String _nickName)
    {
        this._nickName = _nickName;
    }

    public String get_bornDate()
    {
        return _bornDate;
    }

    public void set_bornDate(String _bornDate)
    {
        this._bornDate = _bornDate;
    }

    public String get_mail()
    {
        return _mail;
    }

    public void set_mail(String _mail)
    {
        this._mail = _mail;
    }

    public String get_age()
    {
        return _age;
    }

    public void set_age(String _age)
    {
        this._age = _age;
    }


}
